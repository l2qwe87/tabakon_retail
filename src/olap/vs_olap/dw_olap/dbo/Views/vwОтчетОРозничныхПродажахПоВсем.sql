

CREATE VIEW [dbo].[vwОтчетОРозничныхПродажахПоВсем] as
SELECT
      CAST ([Дата] AS DATE) date_
      ,[Номенклатура]
      ,SUM([Количество]) [Количество]
      ,SUM([Сумма]) [Сумма]
	  ,SUM([Себестоимость]) [Себестоимость]
  FROM [dbo].[ОтчетОРозничныхПродажах]
  GROUP BY
	CAST ([Дата] AS DATE)
    ,[Номенклатура]

